<center><big><b>Gestion des Membres</b></big></center><br>
<li><b>Index</b> : Affiche la liste des membres avec options de classement et formulaire de recherche, il permet d'ajouter, 
d'éditer ou supprimer un membre ainsi que de se rendre dans les différentes parties de la Gestion des Membres.<br>
<br>
<li><b>Ajouter un membre </b> : Pour ajouter un membre, remplissez simplement les champs du Formulaire, vous pouvez lui 
attribuer jusqu'a 3 Teams et un rang si il s’agit d'un membre de votre clan.<br>
<br>
<li><b>Gestion des Teams</b> : Vous permet de créer des Teams avec un nom, un jeu, des Tags et un ordre personnalisés<br>
<br>
<li><b>Gestion des Rangs</b> : Vous permet de créer vos rangs (ex : soldat, général ect...) avec un ordre d'affichage en 
fonction de ce rang.<br>
<br>
<li><b>Membres non validés</b> : Affiche la liste des membres encore non validés, vous pouvez éditer, supprimer ou valider les membres de cette liste.<br>
<br>
<li><b>Bannissement</b> : Vous permet de bannir l'ip, le mail ou le pseudo d'un visiteur ou d'un membre indésirable.<br>
<br>
<br>
<i><b>Note :</b> Il existe 9 niveau d'admin, le "0" étant le visiteur, le "1" et le "2" les membres du site et à partir de "3" les
