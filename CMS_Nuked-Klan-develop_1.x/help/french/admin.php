<i>Bienvenue dans l'aide de Nuked-klan, cette 1ère partie concerne l'index de l'administration</i><br>
<br>
<li><b>Préférences Générales</b> : Ici ce trouve les préférences de votre site comme le Titre et l'url du site mais aussi
différents réglages et infos concernant votre site. Cette partie est très importante pour le bon fonctionnement de votre site nuked-klan.<br>
<br>
<li><b>Gestion des membres</b> : Elle permet d'ajouter, d'éditer ou supprimer un membre, de gérer vos Teams 
et les Rangs Team, de voir et valider les membres en attentes ou encore de bannir/débannir une ip, un pseudo ou un email.<br>
<br>
<li><b>Gestion des jeux</b> : Elle permet d'ajouter, d'éditer ou supprimer les différents jeux en réseaux de votre clan.<br>
<br>
<li><b>Gestion des modules</b> : Elle permet de régler le niveau d'admin et d'accès de chaque module mais aussi de
les désactiver ou les activer.<br>
<br>
<li><b>Gestion des blocks</b> :  Elle permet d'éditer, de créer ou encore de désactiver vos blocks de côtés, centre ou bas dont le block menu.<br>
<br>
<li><b>Gestion des Smileys</b> :  Elle permet d'éditer, d'ajouter ou supprimer vos smilies utilisés dans tous votre site nuked-klan.<br>
<br>
<li><b>Gestion des menus</b> :  Elle permet d'éditer, d'ajouter ou supprimer les liens de vos blocks menu.<br>
<br>
<li><b>Administration Mysql</b> :  Elle permet d'exécuter une requête Mysql ou encore de sauvegarder et optimiser votre base de données.<br>
<br>
<li><b>PHP Info</b> :  Elle permet d'afficher toutes les informations de la configuration de votre hébergeur.<br>
<br>
<li><b>Administration des modules</b> : Cette dernière partie, affiche les modules et leur administration respective.<br> 
<br>
