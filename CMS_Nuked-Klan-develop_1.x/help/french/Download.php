<center><big><b>Administration Téléchargements</b></big></center><br>
<li><b>Index</b> : Affiche la liste des téléchargements avec options de classement, il permet d'éditer, de supprimer ou d'ajouter un fichier au module
 download et de se rendre dans la gestion des catégories, les liens morts et les préférences. Le titre du
fichier est également un lien vers la description du fichier et l'icône un lien directe vers ce fichier ce qui permet de vérifier sa validité.<br>
<br>
<li><b>Ajouter un fichier</b> : Ce formulaire vous permet d'ajouter un téléchargement en remplissant simplement les
champs que vous désirez. Vous pouvez préciser plusieurs Url (lien mirroir) et uploader le fichier (et le screenshoot)
directement à partir de ce formulaire.<br>
<br>
<li><b>Editer un fichier</b> : L'édition permet de modifier toutes les informations du fichier avec en plus la possibilité
de modifier le nombre de téléchargement.<br>
<br>
<li><b>Gestion des catégories</b> : Vous permet de créer des catégories et sous-catégories de téléchargement. 
Ces catégories peuvent avoir un niveau requis pour être visibles et une position précise (ordre).<br>
<br>
<li><b>Liens morts</b> : Affiche les liens morts qui ont été indiqués par vos membres ou vos visiteurs.<br>
<br>
<li><b>Préférences</b> : Vous permet de régler le nombre de téléchargements par page dans le module Download 
et de cacher les champs vides dans la description (c'est à dire les champs que vous n'avez pas renseignés).<br>
<br>
<br>

<br>
<i><b>Note :</b> La fonction d'upload n'est pas activée sur tous les hébergeurs et requière que le répertoire 
"upload/Download" soit en CHMOD 777.<i></body></html>